package com.pbo;

public class Operator_Assignment {
    public static void main(String[] args){

        //Operator Assignments

        //Assignments Operator Penjumlahan
        int a = 1;
        a += 10; //a = a +10;
        System.out.println("Nilai a = " + a);

        //Assignments Operator Pengurangan
        int b = 100;
        b -= 25;
        System.out.println("Nilai b = " + b);

        //Assignments Operator Perkalian
        int c = 100;
        c *= 20;
        System.out.println("Nilai c = " + c);

        //Assignments Operator Pembagian
        int d = 100;
        d /= 20;
        System.out.println("Nilai d = " + d);

        //Assignments Operator Modulus
        int e = 10;
        e %= 7;
        System.out.println("Nilai e = " + e);

    }
}
